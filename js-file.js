function add(num1, num2) {
    return num1 + num2
};

function subtract(numberOne, numberTwo) {
    return numberOne - numberTwo
};
function multiply(num1, num2) {
    return num1 * num2
    
};

function power(base, power) {
    return base ** power
};
function divide(dividend, divisor) {
    return dividend / divisor    
}